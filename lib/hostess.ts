export function getHostessReply(input: string): string {
  const normalized = input.trim().toLowerCase();
  if (!normalized) {
    return "تفضّلي بسؤالكِ، يسعدني إرشادكِ لأجمل تجربة في الصالون ✨";
  }

  for (const rule of RULES) {
    if (rule.keywords.some((kw) => normalized.includes(kw))) {
      return rule.reply;
    }
  }

  return `شكرًا لسؤالكِ 🤍 يمكنني مساعدتكِ في الشعر، البشرة، الأظافر، الحمام المغربي، المساج، والعروض. أو احجزي مباشرة عبر الواتساب.`;
}
